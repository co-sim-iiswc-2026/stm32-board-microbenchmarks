#!/usr/bin/env python3
"""Generate random VFP instruction-sequence capture tests (Level B).

Each emitted file runs a seed-determined sequence of VFPv4-SP instructions
over s0..s15 with seeded operands, then captures the full single-precision FP
register file (s0..s31) + FPSCR. Diffing these against gem5 surfaces pipeline
interactions and multi-instruction decode bugs not covered by single-op tests.

All randomness is Python-side. A fixed seed produces a fixed .cc. The board
is the reference model; the generator never predicts output.

Instruction catalog is deliberately conservative in v1: only ops that don't
need flag/FPSCR context and that can't easily NaN with bounded inputs. No
vdiv, vsqrt, conditionals, or loads/stores (Level C territory).

Files are picked up by the existing file(GLOB) in
benchmark/microbench/CMakeLists.txt. Sweep-script snippet printed to stdout.

Usage:
    python3 verification/gen/fpu_seq.py
"""

import random
import struct
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent.parent
OUT_DIR = REPO_ROOT / "benchmark" / "microbench" / "generated"


def uint_from_float(f):
    return struct.unpack('<I', struct.pack('<f', f))[0]


def float_hex(f):
    return f"0x{uint_from_float(f):08x}u"


# (mnemonic, source count). Destination is always a single-precision reg.
# vfma/vmla are accumulating (Sd += Sn*Sm); fine as long as Sd is in the live
# set, which it always is here (all of s0..s15 are initialized from entry_).
CATALOG = [
    ("vadd.f32", 2),
    ("vsub.f32", 2),
    ("vmul.f32", 2),
    ("vneg.f32", 1),
    ("vabs.f32", 1),
    ("vmov.f32", 1),   # reg-to-reg
    ("vfma.f32", 2),
    ("vmla.f32", 2),
]

SEEDS = [
    0x00000001, 0xDEADBEEF, 0xCAFEF00D, 0xBEEFDEAD,
    0x12345678, 0xABCDEF01, 0x5A5A5A5A, 0xA5A5A5A5,
]
LENGTHS = [10, 20]
NUM_REGS = 16  # s0..s15


def gen_entry_values(rng):
    """16 floats in ±[0.5, 2.0). Bounded magnitude keeps 20-op chains in normals."""
    vals = []
    for _ in range(NUM_REGS):
        sign = 1.0 if rng.random() < 0.5 else -1.0
        mag = 0.5 + rng.random() * 1.5
        vals.append(sign * mag)
    return vals


def gen_instruction(rng):
    mnem, n_src = rng.choice(CATALOG)
    sd = rng.randint(0, NUM_REGS - 1)
    srcs = [rng.randint(0, NUM_REGS - 1) for _ in range(n_src)]
    if n_src == 1:
        return f"{mnem} s{sd}, s{srcs[0]}"
    return f"{mnem} s{sd}, s{srcs[0]}, s{srcs[1]}"


def gen_sequence(rng, length):
    return [gen_instruction(rng) for _ in range(length)]


FILE_TEMPLATE = '''// Auto-generated by verification/gen/fpu_seq.py — do not edit.
// Random VFP sequence, seed=0x{seed:08x}, length={length}.
// Captures s0..s31 (128 B) + FPSCR (4 B) = 132 B into exit_.

#include <cstdint>

#include <ento-bench/bench_config.h>
#include <ento-bench/capture_problem.h>
#include <ento-bench/harness.h>
#include <ento-mcu/clk_util.h>
#include <ento-mcu/systick_config.h>

extern "C" void initialise_monitor_handles(void);

namespace {{

class {cls_name} : public EntoBench::CaptureProblem<{cls_name}, 132>
{{
public:
  void prepare_impl()
  {{
{entry_init}
    uint32_t fpscr_init = 0u;
    __asm__ volatile("vmsr fpscr, %0" : : "r"(fpscr_init));
  }}

  void solve_impl()
  {{
    __asm__ volatile(
      // Zero s0..s31 via d0..d15 so spectator regs don't leak prior state.
      "mov r0, #0\\n\\t"
{zero_fp}
      // Load s0..s15 from entry_ (vldr — avoid vldmia, known gem5 decoder bug).
{entry_load}
      // Random sequence ({length} instructions, seed=0x{seed:08x}).
{seq_block}
      // Capture s0..s31 into exit_[0..127] (vstr — avoid vstmia for same reason).
{store_fp}
      // Capture FPSCR into exit_[128..131].
      "vmrs r0, fpscr\\n\\t"
      "str  r0, [%1, #128]\\n\\t"
      :
      : "r"(entry_), "r"(exit_)
      : "r0", {fp_clobbers}, "memory"
    );
  }}

private:
  alignas(8) uint32_t entry_[16]{{}};
}};

}}  // namespace

int main()
{{
  initialise_monitor_handles();

#ifndef GEM5_SIM
  sys_clk_cfg();
#endif
  SysTick_Setup();
  __enable_irq();
#ifndef GEM5_SIM
  ENTO_BENCH_SETUP();
#endif
  ENTO_BENCH_PRINT_CONFIG();

  {cls_name} problem;
  ENTO_BENCH_HARNESS_TYPE(decltype(problem));
  BenchHarness harness(problem, "{bench_name}");
  harness.run();

  exit(0);
  return 0;
}}
'''


def gen_zero_fp():
    return "\n".join(f'      "vmov d{i}, r0, r0\\n\\t"' for i in range(16))


def gen_store_fp():
    return "\n".join(f'      "vstr.32 s{i}, [%1, #{i*4}]\\n\\t"' for i in range(32))


def gen_entry_init(vals):
    return "\n".join(f"    entry_[{i}] = {float_hex(v)};  // {v}f"
                     for i, v in enumerate(vals))


def gen_entry_load():
    return "\n".join(f'      "vldr.32 s{i}, [%0, #{i*4}]\\n\\t"'
                     for i in range(NUM_REGS))


def gen_seq_block(seq):
    return "\n".join(f'      "{line}\\n\\t"' for line in seq)


def fp_clobber_list():
    return ", ".join(f'"s{i}"' for i in range(32))


def build_test(seed, length):
    """Return (bench_name, entry_vals, seq, cc_content) for one test."""
    rng = random.Random(seed)
    entry = gen_entry_values(rng)
    seq = gen_sequence(rng, length)
    cls_name = f"BenchFpuSeq_s{seed:08x}_n{length}"
    bench_name = f"bench_fpu_seq_s{seed:08x}_n{length}"
    cc = FILE_TEMPLATE.format(
        seed=seed,
        length=length,
        cls_name=cls_name,
        bench_name=bench_name,
        entry_init=gen_entry_init(entry),
        zero_fp=gen_zero_fp(),
        entry_load=gen_entry_load(),
        seq_block=gen_seq_block(seq),
        store_fp=gen_store_fp(),
        fp_clobbers=fp_clobber_list(),
    )
    return bench_name, entry, seq, cc


def gen_sweep_snippet(seeds, lengths):
    lines = ["# Append these to BENCHES / FPU_BENCHES in the sweep scripts:"]
    for seed in seeds:
        for length in lengths:
            lines.append(f"    bench-fpu-seq-s{seed:08x}-n{length}")
    return "\n".join(lines)


MANIFEST_PATH = REPO_ROOT / "verification" / "gen" / "fpu_seq_manifest.txt"


def gen_manifest_entry(bench_name, seed, length, entry_vals, seq):
    lines = [f"[{bench_name}]",
             f"seed    = 0x{seed:08x}",
             f"length  = {length}",
             "entry:"]
    for i, v in enumerate(entry_vals):
        lines.append(f"  s{i:<2} = 0x{uint_from_float(v):08x}  {v}f")
    lines.append("sequence:")
    for instr in seq:
        lines.append(f"  {instr}")
    return "\n".join(lines)


def write_manifest(entries):
    header = ("# fpu_seq manifest — seed, entries, and instruction sequence\n"
              "# for each generated Level B test. Re-generate via\n"
              "# verification/gen/fpu_seq.py. Do not edit.\n\n")
    body = "\n\n".join(entries) + "\n"
    MANIFEST_PATH.write_text(header + body)


def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    manifest_entries = []
    count = 0
    for seed in SEEDS:
        for length in LENGTHS:
            bench_name, entry, seq, cc = build_test(seed, length)
            fname = f"{bench_name}.cc"
            path = OUT_DIR / fname
            path.write_text(cc)
            print(f"  wrote {path.relative_to(REPO_ROOT)}")
            manifest_entries.append(
                gen_manifest_entry(bench_name, seed, length, entry, seq))
            count += 1
    write_manifest(manifest_entries)
    print(f"  wrote {MANIFEST_PATH.relative_to(REPO_ROOT)}")
    print()
    print(f"Generated {count} tests in {OUT_DIR.relative_to(REPO_ROOT)}/")
    print()
    print(gen_sweep_snippet(SEEDS, LENGTHS))


if __name__ == "__main__":
    main()
