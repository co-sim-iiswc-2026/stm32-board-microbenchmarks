#!/usr/bin/env python3
"""Generate repeated-instruction FPU capture tests (Level A, differential testing).

Each emitted file runs one VFP instruction N times in a row with output→input
threading, then captures the full single-precision FP register file (s0..s31)
plus FPSCR into the CaptureProblem exit buffer. Diffing these against gem5
surfaces accumulator drift, back-to-back issue bugs, and FPSCR sticky-flag
accumulation.

Files are picked up by the existing file(GLOB) in
benchmark/microbench/CMakeLists.txt (pattern: generated/bench_fpu_*.cc) — no
CMake edit needed. Sweep-script snippet is printed to stdout.

Usage:
    python3 verification/gen/fpu_repeat.py
"""

import struct
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent.parent
OUT_DIR = REPO_ROOT / "benchmark" / "microbench" / "generated"


def uint_from_float(f):
    return struct.unpack('<I', struct.pack('<f', f))[0]


def float_hex(f):
    return f"0x{uint_from_float(f):08x}u"


# Accumulating-op specs. Each spec threads s0 across iterations.
#   name   : base name (goes into file + class + bench name)
#   asm    : the instruction to repeat (output→input: Sd must equal an operand)
#   entry  : initial values for s0..s<k-1>. For mul-style, s1 is the per-iter
#            multiplier chosen to keep s0 in normal range across N=256 steps.
INSTRUCTIONS = [
    {
        "name":  "vadd_f32",
        "asm":   "vadd.f32 s0, s0, s1",
        "entry": [0.0, 1e-7],
    },
    {
        "name":  "vsub_f32",
        "asm":   "vsub.f32 s0, s0, s1",
        "entry": [0.0, 1e-7],
    },
    {
        "name":  "vmul_f32",
        "asm":   "vmul.f32 s0, s0, s1",
        "entry": [1.0, 1.0000001],
    },
    {
        "name":  "vfma_f32",
        "asm":   "vfma.f32 s0, s1, s2",
        "entry": [0.0, 1.0, 1e-7],
    },
    {
        "name":  "vmla_f32",
        "asm":   "vmla.f32 s0, s1, s2",
        "entry": [0.0, 1.0, 1e-7],
    },
]

N_VALUES = [2, 8, 64, 256]


FILE_TEMPLATE = '''// Auto-generated by verification/gen/fpu_repeat.py — do not edit.
// Tests: {asm} repeated {n} times (output→input threaded).
// Captures s0..s31 (128 B) + FPSCR (4 B) = 132 B into exit_.

#include <cstdint>

#include <ento-bench/bench_config.h>
#include <ento-bench/capture_problem.h>
#include <ento-bench/harness.h>
#include <ento-mcu/clk_util.h>
#include <ento-mcu/systick_config.h>

extern "C" void initialise_monitor_handles(void);

namespace {{

class {cls_name} : public EntoBench::CaptureProblem<{cls_name}, 132>
{{
public:
  void prepare_impl()
  {{
{entry_init}
    uint32_t fpscr_init = 0u;
    __asm__ volatile("vmsr fpscr, %0" : : "r"(fpscr_init));
  }}

  void solve_impl()
  {{
    __asm__ volatile(
      // Zero s0..s31 via d0..d15 so spectator regs don't leak prior state.
      "mov r0, #0\\n\\t"
{zero_fp}
      // Load entry values (vldr — avoid vldmia, known gem5 decoder bug).
{entry_load}
      // Repeat target instruction {n} times.
{repeat_block}
      // Capture s0..s31 into exit_[0..127].
{store_fp}
      // Capture FPSCR into exit_[128..131].
      "vmrs r0, fpscr\\n\\t"
      "str  r0, [%1, #128]\\n\\t"
      :
      : "r"(entry_), "r"(exit_)
      : "r0", {fp_clobbers}, "memory"
    );
  }}

private:
  alignas(8) uint32_t entry_[{n_entry}]{{}};
}};

}}  // namespace

int main()
{{
  initialise_monitor_handles();

#if !defined(GEM5_SIM) && !defined(QEMU_SIM)
  sys_clk_cfg();
#endif
  SysTick_Setup();
  __enable_irq();
#if !defined(GEM5_SIM) && !defined(QEMU_SIM)
  ENTO_BENCH_SETUP();
#endif
  ENTO_BENCH_PRINT_CONFIG();

  {cls_name} problem;
  ENTO_BENCH_HARNESS_TYPE(decltype(problem));
  BenchHarness harness(problem, "{bench_name}");
  harness.run();

  exit(0);
  return 0;
}}
'''


def gen_zero_fp():
    return "\n".join(f'      "vmov d{i}, r0, r0\\n\\t"' for i in range(16))


def gen_store_fp():
    return "\n".join(f'      "vstr.32 s{i}, [%1, #{i*4}]\\n\\t"' for i in range(32))


def gen_entry_init(vals):
    return "\n".join(f"    entry_[{i}] = {float_hex(v)};  // {v}f"
                     for i, v in enumerate(vals))


def gen_entry_load(n_entry):
    return "\n".join(f'      "vldr.32 s{i}, [%0, #{i*4}]\\n\\t"'
                     for i in range(n_entry))


def gen_repeat_block(asm_line, n):
    return "\n".join(f'      "{asm_line}\\n\\t"' for _ in range(n))


def fp_clobber_list():
    return ", ".join(f'"s{i}"' for i in range(32))


def gen_file(spec, n):
    name = spec["name"]
    entry = spec["entry"]
    cls_name = f"BenchFpuRepeat_{name}_n{n}"
    bench_name = f"bench_fpu_repeat_{name}_n{n}"
    return FILE_TEMPLATE.format(
        asm=spec["asm"],
        n=n,
        cls_name=cls_name,
        bench_name=bench_name,
        entry_init=gen_entry_init(entry),
        zero_fp=gen_zero_fp(),
        entry_load=gen_entry_load(len(entry)),
        repeat_block=gen_repeat_block(spec["asm"], n),
        store_fp=gen_store_fp(),
        fp_clobbers=fp_clobber_list(),
        n_entry=len(entry),
    )


def gen_sweep_snippet(specs, n_values):
    lines = ["# Append these to BENCHES / FPU_BENCHES in the sweep scripts:"]
    for s in specs:
        for n in n_values:
            lines.append(f"    bench-fpu-repeat-{s['name'].replace('_','-')}-n{n}")
    return "\n".join(lines)


def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    count = 0
    for spec in INSTRUCTIONS:
        for n in N_VALUES:
            fname = f"bench_fpu_repeat_{spec['name']}_n{n}.cc"
            path = OUT_DIR / fname
            path.write_text(gen_file(spec, n))
            print(f"  wrote {path.relative_to(REPO_ROOT)}")
            count += 1
    print()
    print(f"Generated {count} tests in {OUT_DIR.relative_to(REPO_ROOT)}/")
    print()
    print(gen_sweep_snippet(INSTRUCTIONS, N_VALUES))


if __name__ == "__main__":
    main()
