#!/usr/bin/env python3
"""Generate per-bench .S kernels from bench_spec.yaml.

Output goes to benchmark/multibench/src/generated/. The output is committed
to the repo so reviewers can read kernel_<name>.S directly and verify the
emitted asm matches the design (e.g. "yes, those addresses are PRNG-driven
with seed 0xDEAD").

Run:
    python3 benchmark/multibench/tools/gen_kernels.py

Schema lives in bench_spec.yaml (requires PyYAML); this script enforces:
- Per-kernel section directive (`.text.kernel_<name>`) so --gc-sections drops
  unreferenced kernels and the linker map shows per-kernel size.
- AAPCS prologue/epilogue when the bench touches r4-r11 or s16-s31.
- The wfi archetype produces a stub note; kernel_wfi_100us.S is hand-written
  and lives alongside the generated files (see emit_wfi for details).

Python 3.11+.
"""
from __future__ import annotations

import argparse
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path

import yaml

REPO = Path(__file__).resolve().parents[3]  # repo root
SPEC = REPO / "benchmark" / "multibench" / "tools" / "bench_spec.yaml"
OUT_DIR = REPO / "benchmark" / "multibench" / "src" / "generated"


HEADER = """\
@ Generated by benchmark/multibench/tools/gen_kernels.py.
@ DO NOT EDIT — regenerate via:
@     python3 benchmark/multibench/tools/gen_kernels.py
@
@ Spec source: benchmark/multibench/tools/bench_spec.yaml
@ Bench: {name}
@ Archetype: {archetype}

.syntax unified
.cpu cortex-m4
.fpu fpv4-sp-d16
.thumb
"""

# AAPCS prologue/epilogue helpers
AAPCS_PROLOGUE = {
    "minimal": [],
    "full_int": ["push {r4-r11, lr}"],
    "full_fpu": ["vpush {s16-s31}"],
    "full_int_fpu": ["push {r4-r11, lr}", "vpush {s16-s31}"],
}

AAPCS_EPILOGUE = {
    "minimal": ["bx lr"],
    "full_int": ["pop {r4-r11, pc}"],
    "full_fpu": ["vpop {s16-s31}", "bx lr"],
    "full_int_fpu": ["vpop {s16-s31}", "pop {r4-r11, pc}"],
}


@dataclass
class Bench:
    name: str
    archetype: str
    partition: str
    smoke: bool = False
    needs_warmup: bool = False
    needs_nvic_reset: bool = False
    gem5_safe: bool = True
    aapcs: str = "minimal"
    align: int = 16
    raw: dict = field(default_factory=dict)

    @classmethod
    def from_dict(cls, d: dict, defaults: dict) -> "Bench":
        merged = {**defaults, **d}
        return cls(
            name=d["name"],
            archetype=d["archetype"],
            partition=d["partition"],
            smoke=bool(merged.get("smoke", False)),
            needs_warmup=bool(merged.get("needs_warmup", False)),
            needs_nvic_reset=bool(merged.get("needs_nvic_reset", False)),
            gem5_safe=bool(merged.get("gem5_safe", True)),
            aapcs=merged.get("aapcs", "minimal"),
            align=int(merged.get("align", 16)),
            raw=merged,
        )


def load_spec(path: Path) -> tuple[list[Bench], dict]:
    with open(path) as f:
        doc = yaml.safe_load(f)
    defaults = doc.get("defaults", {}) or {}
    benches = [Bench.from_dict(b, defaults) for b in doc["benches"]]
    return benches, defaults


def emit_data_blocks(blocks: list[dict] | dict | None) -> list[str]:
    """Emit .data/.bss sections for the bench (none/one/many).

    Init policy:
      - non-zero init or explicit init list   -> .data section, .word emitters
      - init == 0 and count <= 16             -> .data section, .word zeros
      - init == 0 and count > 16              -> .bss section, .space (RAM-only,
        no flash footprint, no .data copy at startup)

    The .bss path matters: a 32 KB zero-init buffer in .data overflows RAM
    because both flash storage AND RAM live image are reserved. Putting it
    in .bss costs zero flash and the runtime crt0 zero-fills .bss at boot.
    """
    if blocks is None:
        return []
    if isinstance(blocks, dict):
        blocks = [blocks]
    out: list[str] = []
    for b in blocks:
        sym = b["symbol"]
        kind = b.get("kind", "word")
        count = int(b["count"])
        custom_section = b.get("section")
        init = b.get("init", 0)

        if kind != "word":
            raise ValueError(f"unsupported data kind: {kind}")

        is_bulk_zero = (
            (not isinstance(init, list))
            and int(init) == 0
            and count > 16
            and custom_section is None
        )
        if is_bulk_zero:
            out.append("")
            out.append(f'.section .bss.{sym}, "aw", %nobits')
            out.append(".balign 4")
            out.append(f".global {sym}")
            out.append(f".type   {sym}, %object")
            out.append(f".size   {sym}, {count * 4}")
            out.append(f"{sym}:")
            out.append(f"    .space {count * 4}")
            continue

        section = custom_section or ".data"
        out.append("")
        out.append(f'.section {section}.{sym}, "aw", %progbits')
        out.append(".balign 4")
        out.append(f".global {sym}")
        out.append(f".type   {sym}, %object")
        out.append(f".size   {sym}, {count * 4}")
        out.append(f"{sym}:")
        if isinstance(init, list):
            for v in init:
                out.append(f"    .word 0x{int(v) & 0xFFFFFFFF:08x}")
        else:
            init_word = int(init) & 0xFFFFFFFF
            if init_word == 0:
                out.append(f"    .space {count * 4}")
            else:
                # Spelled-out word table for deterministic cache-warming.
                for _ in range(count):
                    out.append(f"    .word 0x{init_word:08x}")
    return out


def emit_kernel_header(name: str, archetype: str, align: int) -> list[str]:
    return [
        HEADER.format(name=name, archetype=archetype),
        f'.section .text.kernel_{name}, "ax", %progbits',
        f".balign {align}",
        f".global  kernel_{name}",
        f".type    kernel_{name}, %function",
        ".thumb_func",
        f"kernel_{name}:",
    ]


def emit_aapcs(aapcs: str, *, prologue: bool) -> list[str]:
    table = AAPCS_PROLOGUE if prologue else AAPCS_EPILOGUE
    if aapcs not in table:
        raise ValueError(f"unknown aapcs: {aapcs}")
    return [f"    {l}" for l in table[aapcs]]


def emit_rept(b: Bench) -> str:
    raw = b.raw
    instr = raw["instruction"]
    count = int(raw["count"])
    inner_iters = int(raw.get("inner_iters", 1))
    prologue = raw.get("prologue") or []
    data = raw.get("data")

    lines: list[str] = []
    lines += emit_kernel_header(b.name, b.archetype, b.align)
    lines += emit_aapcs(b.aapcs, prologue=True)
    has_pool = any("=" in line for line in prologue)

    outer_pro, inner_pro = (
        _split_prologue_for_inner_loop(prologue, b.aapcs, b.name)
        if inner_iters > 1
        else (list(prologue), [])
    )

    # Outer setup (always runs once). For has_pool, this primes gas's pool.
    for line in outer_pro:
        lines.append(f"    {line}")
    if has_pool:
        # Local label 9 reserved for the pool-flush trampoline.
        lines.append("    b 9f")
        lines.append("    .ltorg")
        lines.append("9:")

    if inner_iters > 1:
        # r12 (ip) is caller-saved per AAPCS — no push/pop. Local label 8
        # reserved for the inner-loop top.
        lines.append(_emit_inner_iter_setup(inner_iters))
        lines.append("8:")
        for line in inner_pro:
            lines.append(f"    {line}")

    lines.append(f"    .rept {count}")
    lines.append(f"    {instr}")
    lines.append("    .endr")

    if inner_iters > 1:
        lines.append("    subs.w r12, r12, #1")
        lines.append("    bne 8b")

    lines += emit_aapcs(b.aapcs, prologue=False)
    lines.append(f".size kernel_{b.name}, .-kernel_{b.name}")
    lines += emit_data_blocks(data)
    lines.append("")
    return "\n".join(lines)


def _emit_inner_iter_setup(n: int) -> str:
    """Load r12 with the inner-loop iteration count.

    `movs` doesn't accept high regs in Thumb-2, so we use `movw` (32-bit
    encoding, 4 bytes) for any non-zero count. Caps at 0xFFFF — beyond that
    you'd need a movw/movt pair, which no microbench should require.
    """
    if not (1 <= n <= 0xFFFF):
        raise ValueError(f"inner_iters={n} out of range [1, 0xFFFF]")
    return f"    movw r12, #{n}"


# Stash registers for in-loop pointer reset. We pick high callee-saved regs
# (r8-r11) so they don't collide with the bench's operand regs (typically
# r4-r7). full_int aapcs already pushes these, so no extra prologue cost.
_LDR_EQ_RE = re.compile(r"^\s*ldr\s+(\w+)\s*,\s*=([\w0-9]+)\s*$")
_STASH_REGS = ["r11", "r10", "r9", "r8"]


def _split_prologue_for_inner_loop(
    prologue: list[str], aapcs: str, bench_name: str,
) -> tuple[list[str], list[str]]:
    """Split a prologue into (outer_setup, inner_reset) for inner_iters > 1.

    Each `ldr Rd, =sym` line in the prologue is rewritten so the symbol
    address is loaded ONCE outside the loop and stashed in a callee-saved
    register; the inner reset then uses a register-to-register move. This
    sidesteps the literal-pool ±4 KB range constraint on big rept bodies
    (e.g. ldm_burst_8k's 8 KB body would push the in-loop pool out of range).

    For minimal aapcs benches the stash regs aren't pushed, so we don't have
    safe scratch — we skip the inner reset entirely. The body must not
    permanently mutate prologue-set regs (true for the FPU constant-load
    benches: vdiv_hard / vsqrt_hard).
    """
    has_pool = any("=" in line for line in prologue)
    if not has_pool:
        # Plain register sets (movs, vmov.f32 #imm). Re-execute as-is.
        return prologue, list(prologue)

    if aapcs == "minimal":
        # No callee-saved scratch available; skip inner reset. The bench
        # author must ensure the body doesn't permanently mutate the
        # prologue's operand regs (vdiv_hard etc. only read s1/s2).
        return prologue, []

    outer: list[str] = []
    inner: list[str] = []
    stash_idx = 0
    for line in prologue:
        m = _LDR_EQ_RE.match(line.strip())
        if m:
            rd, sym = m.group(1), m.group(2)
            if stash_idx >= len(_STASH_REGS):
                raise ValueError(
                    f"{bench_name}: too many `ldr =sym` prologue lines for "
                    f"stash registers ({_STASH_REGS})"
                )
            stash = _STASH_REGS[stash_idx]
            stash_idx += 1
            outer.append(f"ldr {rd}, ={sym}")
            outer.append(f"mov {stash}, {rd}")
            inner.append(f"mov {rd}, {stash}")
        else:
            outer.append(line)
            inner.append(line)
    return outer, inner


def emit_mixed(b: Bench) -> str:
    raw = b.raw
    count = int(raw["count"])
    inner_iters = int(raw.get("inner_iters", 1))
    pattern = raw["pattern"]
    pattern_prologue = raw.get("pattern_prologue") or []
    data = raw.get("data")

    lines: list[str] = []
    lines += emit_kernel_header(b.name, b.archetype, b.align)
    lines += emit_aapcs(b.aapcs, prologue=True)
    has_pool = any("=" in line for line in pattern_prologue)

    outer_pro, inner_pro = (
        _split_prologue_for_inner_loop(pattern_prologue, b.aapcs, b.name)
        if inner_iters > 1
        else (list(pattern_prologue), [])
    )

    for line in outer_pro:
        lines.append(f"    {line}")
    if has_pool:
        lines.append("    b 9f")
        lines.append("    .ltorg")
        lines.append("9:")

    if inner_iters > 1:
        lines.append(_emit_inner_iter_setup(inner_iters))
        lines.append("8:")
        for line in inner_pro:
            lines.append(f"    {line}")

    lines.append(f"    .rept {count}")
    for line in pattern:
        lines.append(f"    {line}")
    lines.append("    .endr")

    if inner_iters > 1:
        lines.append("    subs.w r12, r12, #1")
        lines.append("    bne 8b")

    lines += emit_aapcs(b.aapcs, prologue=False)
    lines.append(f".size kernel_{b.name}, .-kernel_{b.name}")
    lines += emit_data_blocks(data)
    lines.append("")
    return "\n".join(lines)


def emit_art_loop(b: Bench) -> str:
    """Backward-branch loop body of `body_bytes` bytes, iterated to
    `total_fetches` total instruction fetches.

    Body is built from 16-bit Thumb NOPs (2 bytes each), so a 64 B body is
    32 NOPs + 1 trailing branch (4 B since cbnz/subs+bne is 32-bit) ≈ matches
    requested size to within a few bytes. We size the body in NOPs only, with
    `body_nops = body_bytes // 2 - 2`, accounting for the `subs r6, #1`
    (16-bit) and `bne` (16-bit-encoded if displacement < 256 B, else 32-bit).
    """
    raw = b.raw
    body_bytes = int(raw["body_bytes"])
    total_fetches = int(raw["total_fetches"])

    # Reserve 4 B for the `subs r6, #1` (2 B) and `bne 1b` (2 B) trailer.
    # On larger bodies the displacement may force a 32-bit B encoding (4 B);
    # the assembler picks. We round body_nops down conservatively so total
    # body size ≤ requested (rather than overshoot a 4 KB boundary).
    nop_bytes = max(0, body_bytes - 4)
    body_nops = nop_bytes // 2

    iters = max(1, total_fetches // (body_nops + 2))

    lines: list[str] = []
    lines += emit_kernel_header(b.name, b.archetype, b.align)
    # AAPCS: we only touch r6, which is r4-r11 (callee-saved) — full_int.
    aapcs = "full_int"
    lines += emit_aapcs(aapcs, prologue=True)
    if iters <= 0xFF:
        lines.append(f"    movs r6, #{iters}")
    elif iters <= 0xFFFF:
        lines.append(f"    movw r6, #{iters}")
    else:
        lines.append(f"    movw r6, #{iters & 0xFFFF}")
        lines.append(f"    movt r6, #{(iters >> 16) & 0xFFFF}")
    lines.append("1:")
    lines.append(f"    .rept {body_nops}")
    lines.append("    nop")
    lines.append("    .endr")
    lines.append("    subs r6, r6, #1")
    lines.append("    bne 1b")
    lines += emit_aapcs(aapcs, prologue=False)
    lines.append(f".size kernel_{b.name}, .-kernel_{b.name}")
    lines.append("")
    return "\n".join(lines)


def emit_bp_alternating(b: Bench) -> str:
    """Backward loop with an inner forward conditional whose target depends
    on iter parity. Pattern (per iteration):

        ands r1, r6, #1   @ Z=1 when counter is even, Z=0 when odd
        bne 2f            @ taken on odd iters (forward, mispredicted ~50%)
        nop               @ executed on even iters
      2:
        subs r6, r6, #1
        bne 1b            @ backward, always taken

    5 instructions per iter; ~50% of the inner BNEs mispredict on a static
    2-bit predictor, exposing the misprediction-energy term.
    """
    raw = b.raw
    iters = int(raw["iters"])

    lines: list[str] = []
    lines += emit_kernel_header(b.name, b.archetype, b.align)
    aapcs = "full_int"
    lines += emit_aapcs(aapcs, prologue=True)
    if iters <= 0xFF:
        lines.append(f"    movs r6, #{iters}")
    elif iters <= 0xFFFF:
        lines.append(f"    movw r6, #{iters}")
    else:
        lines.append(f"    movw r6, #{iters & 0xFFFF}")
        lines.append(f"    movt r6, #{(iters >> 16) & 0xFFFF}")
    lines.append("1:")
    lines.append("    ands r1, r6, #1")
    lines.append("    bne 2f")
    lines.append("    nop")
    lines.append("2:")
    lines.append("    subs r6, r6, #1")
    lines.append("    bne 1b")
    lines += emit_aapcs(aapcs, prologue=False)
    lines.append(f".size kernel_{b.name}, .-kernel_{b.name}")
    lines.append("")
    return "\n".join(lines)


def emit_random_load(b: Bench) -> str:
    """PRNG-driven LDR sequence over a buffer of `buffer_bytes`. Offsets are
    pre-computed (deterministic for `seed`) and emitted as a `.word` table;
    kernel walks the table and dereferences `buf + offset`. Per the design
    note, this lets reviewers cat the .S and verify "yes, those addresses
    are PRNG-driven with seed 0xDEAD."
    """
    import random

    raw = b.raw
    count = int(raw["count"])
    inner_iters = int(raw.get("inner_iters", 1))
    buf_bytes = int(raw["buffer_bytes"])
    seed = int(raw.get("seed", 0xDEAD))
    shared = raw.get("shared_buffer_symbol")

    rng = random.Random(seed)
    mask = (buf_bytes - 1) & ~0x3  # word-align
    offsets = [rng.randrange(buf_bytes) & mask for _ in range(count)]

    buf_sym = shared if shared else f"random_buf_{b.name}"

    lines: list[str] = []
    lines += emit_kernel_header(b.name, b.archetype, b.align)
    lines += emit_aapcs(b.aapcs, prologue=True)
    # Prime the literal pool from outside the inner loop, then trampoline
    # so the pool flushes near the function start.
    lines.append(f"    ldr r0, ={buf_sym}")
    lines.append(f"    ldr r1, =random_offsets_{b.name}")
    lines.append("    b 9f")
    lines.append("    .ltorg")
    lines.append("9:")

    if inner_iters > 1:
        lines.append(_emit_inner_iter_setup(inner_iters))
        lines.append("8:")

    # Reset r0/r1/r2 every outer iter so the offset walk restarts at the
    # head of the table (r1 advances via post-inc, r2 decrements). r0 reset
    # is symmetric / cheap.
    lines.append(f"    ldr r0, ={buf_sym}")
    lines.append(f"    ldr r1, =random_offsets_{b.name}")
    lines.append(f"    movw r2, #{count & 0xFFFF}")
    lines.append("1:")
    lines.append("    ldr r3, [r1], #4")     # next offset
    lines.append("    ldr r4, [r0, r3]")     # dereference
    lines.append("    subs r2, r2, #1")
    lines.append("    bne 1b")

    if inner_iters > 1:
        lines.append("    subs.w r12, r12, #1")
        lines.append("    bne 8b")

    lines += emit_aapcs(b.aapcs, prologue=False)
    lines.append(f".size kernel_{b.name}, .-kernel_{b.name}")

    # Emit the data: PRNG-seeded offsets, then the actual buffer.
    lines.append("")
    lines.append(f'.section .data.random_offsets_{b.name}, "aw", %progbits')
    lines.append(".balign 4")
    lines.append(f".global random_offsets_{b.name}")
    lines.append(f"random_offsets_{b.name}:")
    for off in offsets:
        lines.append(f"    .word 0x{off:08x}")

    if not shared:
        # Only emit a private random_buf_<name> when the spec didn't ask
        # to share an existing pool symbol (mem_pool_a/b live in
        # kernel_mem_pool.S so they can be linked once across all kernels).
        lines.append("")
        lines.append(f'.section .bss.random_buf_{b.name}, "aw", %nobits')
        lines.append(".balign 4")
        lines.append(f".global random_buf_{b.name}")
        lines.append(f"random_buf_{b.name}:")
        lines.append(f"    .space {buf_bytes}")
        lines.append("")
    return "\n".join(lines)


def emit_wfi(b: Bench) -> str:
    """Hand-written archetype — codegen emits a stub that asserts the file
    must be hand-maintained. The actual kernel_wfi_<wake>us.S sits next to
    the generated files (committed). We still emit a placeholder header
    listing the wake_us so the diff is consistent.
    """
    raw = b.raw
    wake_us = int(raw["wake_us"])
    lines = [
        f"@ kernel_{b.name} is HAND-WRITTEN — see notes in",
        f"@ benchmark/multibench/src/generated/kernel_{b.name}_handwritten.S.",
        f"@ wake_us={wake_us}",
        "@ The codegen does NOT replace it; this stub exists only to keep",
        "@ the generated/ directory consistent across regenerations.",
        "",
    ]
    return "\n".join(lines)


def emit_kernel(b: Bench) -> tuple[str, str]:
    if b.archetype == "rept":
        return f"kernel_{b.name}.S", emit_rept(b)
    if b.archetype == "mixed":
        return f"kernel_{b.name}.S", emit_mixed(b)
    if b.archetype == "art_loop":
        return f"kernel_{b.name}.S", emit_art_loop(b)
    if b.archetype == "bp_alternating":
        return f"kernel_{b.name}.S", emit_bp_alternating(b)
    if b.archetype == "random_load":
        return f"kernel_{b.name}.S", emit_random_load(b)
    if b.archetype == "wfi":
        return f"kernel_{b.name}.note", emit_wfi(b)
    raise ValueError(f"unknown archetype: {b.archetype}")


def write_table_header(benches: list[Bench]) -> str:
    """Emit a C++ header listing `extern "C"` decls for every kernel + the
    per-partition BENCHES tables. Included by multibench_*_main.cc.
    """
    by_part: dict[str, list[Bench]] = {"core": [], "memory": []}
    for b in benches:
        by_part[b.partition].append(b)

    out: list[str] = []
    out.append("// Auto-generated by benchmark/multibench/tools/gen_kernels.py")
    out.append("// DO NOT EDIT — regenerate via the same script.")
    out.append("#ifndef MULTIBENCH_TABLES_H")
    out.append("#define MULTIBENCH_TABLES_H")
    out.append("")
    out.append("#include <array>")
    out.append("#include <ento-bench/multi_harness.h>")
    out.append("")
    out.append('extern "C" {')
    for b in benches:
        out.append(f"  void kernel_{b.name}(void);")
    out.append("}")
    out.append("")

    for part, items in by_part.items():
        n = len(items)
        out.append(f"// Partition: {part}  ({n} benches)")
        out.append(
            f"inline constexpr std::array<EntoBench::BenchEntry, {n}> "
            f"BENCHES_{part.upper()} = {{{{"
        )
        for b in items:
            warmup = "true" if b.needs_warmup else "false"
            nvic = "true" if b.needs_nvic_reset else "false"
            out.append(
                f'    {{ "{b.name}", kernel_{b.name}, {warmup}, {nvic} }},'
            )
        out.append("}};")
        out.append("")
    out.append("#endif  // MULTIBENCH_TABLES_H")
    out.append("")
    return "\n".join(out)


def write_smoke_table_header(benches: list[Bench]) -> str:
    """Emit a smaller smoke-only header — just the 5-bench subset."""
    smoke = [b for b in benches if b.smoke]
    out: list[str] = []
    out.append("// Auto-generated smoke-bench table (subset of all kernels).")
    out.append("#ifndef MULTIBENCH_SMOKE_TABLE_H")
    out.append("#define MULTIBENCH_SMOKE_TABLE_H")
    out.append("")
    out.append("#include <array>")
    out.append("#include <ento-bench/multi_harness.h>")
    out.append("")
    out.append('extern "C" {')
    for b in smoke:
        out.append(f"  void kernel_{b.name}(void);")
    out.append("}")
    out.append("")
    n = len(smoke)
    out.append(
        f"inline constexpr std::array<EntoBench::BenchEntry, {n}> "
        f"BENCHES_SMOKE = {{{{"
    )
    for b in smoke:
        warmup = "true" if b.needs_warmup else "false"
        nvic = "true" if b.needs_nvic_reset else "false"
        out.append(
            f'    {{ "{b.name}", kernel_{b.name}, {warmup}, {nvic} }},'
        )
    out.append("}};")
    out.append("")
    out.append("#endif  // MULTIBENCH_SMOKE_TABLE_H")
    out.append("")
    return "\n".join(out)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--smoke-only",
        action="store_true",
        help="Only emit kernels marked smoke: true (5-bench subset).",
    )
    parser.add_argument(
        "--out-dir", default=str(OUT_DIR), help="Where to write the .S files"
    )
    args = parser.parse_args()

    benches, _defaults = load_spec(SPEC)
    out = Path(args.out_dir)
    out.mkdir(parents=True, exist_ok=True)

    if args.smoke_only:
        target = [b for b in benches if b.smoke]
    else:
        target = benches

    written = []
    for b in target:
        fname, content = emit_kernel(b)
        path = out / fname
        path.write_text(content)
        written.append(fname)

    # Always emit the full tables header (lists every kernel including ones
    # not regenerated this pass — when smoke-only, the header would point at
    # missing .S files, so we gate on full mode).
    if not args.smoke_only:
        (out / "multibench_tables.h").write_text(write_table_header(benches))
    # Smoke header always refreshes.
    (out / "multibench_smoke_table.h").write_text(
        write_smoke_table_header(benches)
    )

    print(f"Wrote {len(written)} kernel(s) to {out}")
    for f in written:
        print(f"  {f}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
